package ex02_loop;

public class Quiz01 {

	public static void main(String[] args) {

		// 평점에 따른 별(★) 출력하기
		
		int star = 3; // 초기값
		String res = ""; //초기값
		
			for(int n = 1; n <= star; n++) // 연산
				res += "★"; // 연산 후 res에 연산한 수 만큼 ★로 채움
			
			System.out.println(res); // 연산 후 출력
				
			
		
		
		
		
		
		
		
		
		
		
		
	}

}
